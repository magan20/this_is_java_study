package ch01.sec12;

public class Calculator {
    public static void main(String[] args) {
        int x;						//변수 x 선언
        x = 1;						//변수 x에 1 값을 저장
        int y = 2;					//변수 y를 선언하고 2 값을 저장
        int result = x + y;			//변수 result 를 선언하고 변수 x와 y를 더한 값을 저장
        System.out.println(result);	//콘솔에 변수의 값을 출력하는 println() 메소드 호출
    }
}